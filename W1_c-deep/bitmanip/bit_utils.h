/**
 * bit_utils.h — 嵌入式位操作工具库
 *
 * 提供寄存器位操作的标准宏定义。
 * 这些是底层驱动开发中最常用的基础操作。
 *
 * 适用平台: 任何支持标准 C 的 MCU 或模拟环境
 */

#ifndef BIT_UTILS_H
#define BIT_UTILS_H

#include <stdint.h>

/*
 * ==============================================================
 *  单 bit 操作（三板斧）
 *
 *  原理:
 *    (1 << n)  构造一个只有第 n 位为 1 的掩码
 *    SET_BIT   用 | 将目标位置 1
 *    CLR_BIT   用 & ~ 将目标位清 0（先取反产生"目标位0，其他位1"）
 *    GET_BIT   用 & 检测目标位（结果非 0 则为 1）
 *
 *  嵌入式工程师 99% 的寄存器操作都逃不出这三行。
 * ==============================================================
 */

/** 将寄存器的第 n 位置 1 */
#define SET_BIT(reg, n)     ((reg) |= (1U << (n)))

/** 将寄存器的第 n 位置 0 */
#define CLR_BIT(reg, n)     ((reg) &= ~(1U << (n)))

/** 读取寄存器的第 n 位 (返回 0 或 1) */
#define GET_BIT(reg, n)     (((reg) >> (n)) & 1U)

/*
 * ==============================================================
 *  进阶操作
 * ==============================================================
 */

/** 切换寄存器的第 n 位 (1→0, 0→1) */
#define TOGGLE_BIT(reg, n)  ((reg) ^= (1U << (n)))

/** 构造只有第 n 位为 1 的掩码 */
#define BIT_MASK(n)         (1U << (n))

/**
 * 构造位段掩码 (第 l 位到第 h 位, 含两端)
 * 例如: BIT_RANGE_MASK(5, 2) = 0b00111100
 * 可用于一次操作多 bit 的寄存器域
 */
#define BIT_RANGE_MASK(h, l) \
    (((~0U) << (l)) & (~(~0U << (h + 1))))

/**
 * 将值 val 写入寄存器的第 l 到 h 位
 * 先清除目标位段，再写入新值
 */
#define WRITE_BITS(reg, h, l, val) \
    ((reg) = ((reg) & ~BIT_RANGE_MASK(h, l)) | ((val) << (l)))

#endif /* BIT_UTILS_H */
